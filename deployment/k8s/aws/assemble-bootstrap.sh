#!/bin/bash
# =============================================================================
# assemble-bootstrap.sh
#
# Produces a self-contained aws-bootstrap.sh for release by inlining all
# sourced helper scripts. The output can run without a repo checkout.
#
# Reads @source markers in aws-bootstrap.sh and replaces the source block
# with the file contents (stripping shebangs and main guards).
#
# Usage: ./assemble-bootstrap.sh [--output <path>]
#   Default output: deployment/k8s/aws/dist/aws-bootstrap.sh
# =============================================================================
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "$SCRIPT_DIR/../../.." && pwd)"
BOOTSTRAP="$SCRIPT_DIR/aws-bootstrap.sh"
OUTPUT="${SCRIPT_DIR}/dist/aws-bootstrap.sh"

while [[ $# -gt 0 ]]; do
  case "$1" in
    --output) OUTPUT="$2"; shift 2 ;;
    *) echo "Unknown arg: $1" >&2; exit 1 ;;
  esac
done

mkdir -p "$(dirname "$OUTPUT")"

# Collect @source paths from the bootstrap script
mapfile -t SOURCE_PATHS < <(grep '# @source ' "$BOOTSTRAP" | sed 's/.*# @source //')

# Validate all source files exist
for rel_path in "${SOURCE_PATHS[@]}"; do
  abs_path="$REPO_ROOT/$rel_path"
  [[ -f "$abs_path" ]] || { echo "Missing source file: $abs_path" >&2; exit 1; }
done

# Extract script body: strip shebang, set -e, and the main guard block at the end
extract_body() {
  local file="$1"
  # Remove shebang, standalone set lines, and everything from the main guard to EOF
  sed -E \
    -e '1{/^#!/d}' \
    -e '/^set -[a-z]+$/d' \
    -e '/^if \[\[ "\$\{BASH_SOURCE\[0\]\}" == "\$0" \]\]; then$/,$ d' \
    "$file"
}

{
  echo '#!/bin/bash'
  echo '# ============================================================================='
  echo '# aws-bootstrap.sh (assembled release artifact)'
  echo '#'
  echo '# Self-contained — no repo checkout required.'
  echo "# Generated by: deployment/k8s/aws/assemble-bootstrap.sh"
  echo '# Source files:'
  echo "#   deployment/k8s/aws/aws-bootstrap.sh"
  for rel_path in "${SOURCE_PATHS[@]}"; do
    echo "#   $rel_path"
  done
  echo '# ============================================================================='
  echo ''

  # Emit inlined source files
  for rel_path in "${SOURCE_PATHS[@]}"; do
    abs_path="$REPO_ROOT/$rel_path"
    basename="$(basename "$rel_path")"
    echo "# --- BEGIN inlined: $basename ---"
    extract_body "$abs_path"
    echo "# --- END inlined: $basename ---"
    echo ""
  done

  # Emit the main bootstrap script, removing the source block
  sed -E \
    -e '1{/^#!/d}' \
    -e '/^# --- source helper scripts/,/^fi$/d' \
    "$BOOTSTRAP"

} > "$OUTPUT"

chmod +x "$OUTPUT"
echo "Assembled: $OUTPUT ($(wc -l < "$OUTPUT") lines)"
